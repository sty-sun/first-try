package Purpose_code_generator;

public class Info {
    private String name;
    private int location;

    public Info() {
    }

    public String getName() {
        return name;
    }

    public int getLocation() {
        return location;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setLocation(int location) {
        this.location = location;
    }
}
